'use strict';

function getSum(a,b){
   return a+b;
}
function getMultiplying(a,b){
   return a*b;
}
function getDividing(a,b){
   return a/b;
}
function getSubstraction(a,b){
   return a-b;
}

console.log(getSum(2,3));
console.log(getMultiplying(3,4));
console.log(getDividing(5,3));
console.log(getSubstraction(10,7));
